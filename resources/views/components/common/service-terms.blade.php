                استثمارات أو نفقات أو التزامات من جانب المستخدم فيما يتعلق بهذه الشروط أو استخدام المستخدم للخدمات أو
                وصوله إليها؛ و/أو (ج) أي وصول غير مصرح به إلى أو تغيير أو حذف أو إتلاف أو تلف أو خسارة أو فشل في تخزين
