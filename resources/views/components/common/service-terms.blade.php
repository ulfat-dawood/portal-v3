                أثير الأخرى. تقوم أثير بتوفير خدمات تجريبية حتى تتمكن أثير من جمع ملاحظات المستخدم، وباستخدام هذه
                الخدمات التجريبية، يوافق المستخدم على أنه يجوز لأثير الاتصال بالمستخدم لجمع مثل هذه التعليقات.
                تعتبر الخدمات التجريبية جزء من معلومات أثير السرية وتخضع لالتزامات السرية تحت هذه الشروط.
                المدفوعات
                عندما تحجز موعدًا مع مقدم الخدمة، فإن أثير توفر النظام وتكون مسؤولة عن توفر النظام فقط – وغير مسؤولة عن
                تقديم الخدمة أو جودته التي يقدمها مزود الخدمة. عندما تحجز موعدًا، فإنك توافق على دفع تكلفة الخدمات، بما
                في ذلك أي رسوم وضرائب قد يتم تطبيقها.
                بالنسبة لبعض الخدمات، قد يُطلب منك سداد دفعة مقدمة و / أو سداد دفعة في وقت تقديم الخدمة.
                • إذا تم الدفع عن طريق أثير (أو في بعض الحالات الشركة التابعة لنا) فسنكون مسؤولين عن إدارة مدفعاتك وضمان
                إتمام معاملتك مع مزود الخدمة. في هذه الحالة، يشكل الدفع الخاص بك تسوية نهائية للسعر "المستحق والمستحق
                الدفع".
                • إذا قام مزود الخدمة بفرض رسوم عليك (أي الدفع عند الوصول)، فسيكون الدفع عادةً شخصيًا في بداية تقديم
                الخدمة، ولكن قد يتم أيضًا (على سبيل المثال) الخصم من بطاقتك الائتمانية عند الحجز عبر الإنترنت. يعتمد هذا
                على سياسة الدفع مقدمًا لمقدم الخدمة كما تم إبلاغك بها عند عملية الحجز.
                إذا طلب مقدم الخدمة دفعة مقدمة، فقد يتم أخذها أو تفويضها مسبقًا عند إجراء الحجز، واعتمادًا على سياسة
                الدفع مقدمًا لمقدم الخدمة، فقد يكون غير قابل للاسترداد. قبل الحجز، يرجى قراءة سياسة مقدم الخدمة بعناية،
                والتي لا صلة لنا بها ولسنا مسؤولين عنها.
                إصدار الفاتورة الإلكترونية سيتم من قبل أثير كدليل على الدفع؛ ولكن سيتم تسليم الخدمة من قبل مزود الخدمة.
                قد نقوم بتخزين تفاصيل طريقة الدفع الخاصة بك للمعاملات المستقبلية بعد الحصول على موافقتك.
                إذا كنت تعلم أو تشتبه في أي احتيال أو استخدام غير مصرح به لطريقة الدفع الخاصة بك، فيرجى الاتصال بنا على
                الفور لتمكيننا من اتخاذ الإجراءات اللازمة مع قسم الدفع المعني.

