                الخدمات متاحة لأي مستخدم.
                الأولوية 2- عالية
                تعطل النظام و/أو الخدمات الرئيسية بشكل كامل أو جزئي؛ يمكن تجاوز العطل من قبل العميل. تؤثر المشكلة على
                عدد من المستخدمين.
