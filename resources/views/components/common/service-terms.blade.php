                ويوافق المستخدم على أن عمليات الربط الأساسية هذه ستكون جزءًا من النظام و/أو الخدمات؛ أو (ب) كربط مخصص
                بناء على طلب العميل. ومع أن أثير ستبذل أفضل جهودها المعقولة للحفاظ على التوافر المستمر لهذه الخصائص، إلا
                أن أثيرلا تستطيع ضمان استمرار توفر هذه الخصائص، وقد تتوقف عن تقديمها دون منح المستخدم أي استرداد أو
                ائتمان أو أي تعويض آخر في حال -على سبيل المثال لا الحصر- توقف مزود نظام أو تطبيق الطرف الثالث عن إتاحة
                نظام أو تطبيق الطرف الثالث للربط مع النظام أو الخدمات بطريقة مقبولة لدى أثير.
                الصيانة
                تقوم أثير بصيانة كل من النظام و/أو الخدمات من خلال الدعم الفني والترقيات على النحو التالي:
                الدعم الفني: تستلم أثير تذاكر الدعم الفني من المستخدم على مدار ٢٤ ساعة – ٧ أيام في الأسبوع. ستبذل أثير
                قصارى جهدها للرد على جميع التذاكر في الوقت المناسب وبطريقة فعالة. في جميع الحالات وما لم تنص اتفاقية
                مستوى الخدمة على خلاف ذلك، ستطبق أثير أوقات الاستجابة التالية فيما يتعلق بتذاكر الدعم الفني للعميل بناءً
                على مستوى الأولوية:

                مستوى الأولوية
                الوصف
                الأولوية 1 - عالية جدا
