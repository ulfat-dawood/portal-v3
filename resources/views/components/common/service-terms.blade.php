                أعطال في وحدة من الخدمات: يمكن تجاوز العطل من قبل العميل. تم مقاطعة سير عمل المستخدمين. تؤثر المشكلة على
                عدد من المستخدمين.
                الأولوية 4 – منخفضة
                أعطال في وِحدة من الخدمات: يمكن تجاوز العطل من قبل العميل. تمت مقاطعة سير عمل المستخدم. تؤثر المشكلة على
                مستخدم واحد فقط.


